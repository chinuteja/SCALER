'''
Given a sorted array of integers A (0-indexed) of size N, 
find the left most and the right most index of a given integer B in the array A.
Return an array of size 2, such that 
          First element = Left most index of B in A
          Second element = Right most index of B in A.
If B is not found in A, return [-1, -1].

Note : Your algorithm's runtime complexity must be in the order of O(log n).

 A = [5, 7, 7, 8, 8, 10]
 B = 8
Output 
	 [3, 4]
 The first occurrence of 8 in A is at index 3.
 The last occurrence of 8 in A is at index 4.
 ans = [3, 4]
'''

class Solution:
    # @param A : tuple of integers
    # @param B : integer
    # @return a list of integers

    def search(self,A,n,k,flag):

    	l = 0
    	h = n - 1
    	ans = -1
    	while (l <= h):

    		mid = (l + h)//2

    		if (A[mid] == k):
    			
    			ans = mid
    			if flag == True: ## for first occurance
    				h = mid - 1  
    			if flag == False: ## for last occurance
    				l = mid + 1


    		elif(A[mid] < k):
    			l = mid + 1
    		else:
    			h = mid - 1
    	return ans
    def searchRange(self, A, B):

 
    	output = [self.search(A,len(A),B,True),self.search(A,len(A),B,False)]

    	return output


